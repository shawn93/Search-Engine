
public class ListNode
{
    private Object data;
    private ListNode next;

    
    public ListNode(Object d)
    {
        data = d;
        next = null;
    }

    public ListNode(Object d, ListNode n)
    {
        data = d;
        next = n;
    }
    
    
    public Object data()
    {
        return data;
    }
    
    public ListNode next()
    {
        return next;
    }
    
    public void setNext(ListNode newNext)
    {
        this.next = newNext;
    }
    
    public void setData(Object newData)
    {
        this.data = newData;
    }

}