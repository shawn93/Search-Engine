class Edge 
{
    public int neighbor;
    public int cost;
    public Edge next;

    public Edge(int neighbor, Edge next) 
    {
	this.neighbor = neighbor;
	this.next = next;
	cost = 1;
    }

    public Edge(int neighbor, Edge next, int cost) 
    {
	this.neighbor = neighbor;
	this.next = next;
	this.cost = cost;
    }
}